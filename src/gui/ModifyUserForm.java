    private void modifyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifyButtonActionPerformed
        try
        {
            email = emailField.getText();
            passwordUser = passwordField.getText();
            
            if(customerComboBox.getSelectedItem() == "Individual")
            {
                customer = "individual";
            }
            else if(customerComboBox.getSelectedItem() == "Business")
            {
                customer = "business";
            }
            else if(customerComboBox.getSelectedItem() == "Guest")
            {
                customer = "guest";
            }
            else if(customerComboBox.getSelectedItem() == "Employee")
            {
                customer = "employee";
            }

            errorLabel.setVisible(false);

            user.setEmail(email);
            user.setPassword(passwordUser);
            user.setCustomer(customer);

            usersDAO.saveUser(user);

            setVisible(false);
        }
        catch(NumberFormatException e)
        {
            errorLabel.setVisible(true);
        }
    }//GEN-LAST:event_modifyButtonActionPerformed

